#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c <= 'Z' && c >= 'A') || (c <= 'z' && c >= 'a'))
		return (1);
	else if (c <= '9' && c >= '0')
		return (1);
	return (0);
}
